CREATE TABLE PRODUTOS (
ID_PRODUTO INT PRIMARY KEY AUTO_INCREMENT,
NOME VARCHAR(155) NOT NULL,
PRECO_UNICO DECIMAL(10, 2) NOT NULL,
CATEGORIA VARCHAR(155) NOT NULL
);

