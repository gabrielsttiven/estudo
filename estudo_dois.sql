CREATE VIEW ESTOQUE_CRITICO AS
SELECT PRODUTOS.NOME AS NOME_PRODUTO, ESTOQUE.QUANT
FROM PRODUTOS
JOIN ESTOQUE ON PRODUTOS.ID_PRODUTO = ESTOQUE.ID_PRODUTO 
WHERE ESTOQUE.QUANT < 50; 

